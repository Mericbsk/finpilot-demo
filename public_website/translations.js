function changeLanguage(lang) {
    // 1. Çevirileri uygula
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' && element.getAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // 2. Dil seçim kutusunu güncelle (Eğer sayfa yenilendiyse doğru dilde kalsın)
    const langSelect = document.getElementById('language-select');
    if (langSelect && langSelect.value !== lang) {
        langSelect.value = lang;
    }

    // 3. HTML lang attribute'unu güncelle (SEO ve tarayıcı için)
    document.documentElement.lang = lang;

    // 4. Tercihi kaydet
    localStorage.setItem('finpilot_lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('finpilot_lang') || 'en';
    const langSelect = document.getElementById('language-select');
    if(langSelect) {
        langSelect.value = savedLang;
        langSelect.addEventListener('change', (e) => {
            changeLanguage(e.target.value);
        });
    }
    changeLanguage(savedLang);

    // Beta Form Handler
    const betaForm = document.getElementById('betaForm');
    if(betaForm) {
        betaForm.addEventListener('submit', async (e) => {
            e.preventDefault();
            
            const form = e.target;
            const data = new FormData(form);
            const action = form.action;

            try {
                const response = await fetch(action, {
                    method: form.method,
                    body: data,
                    headers: {
                        'Accept': 'application/json'
                    }
                });

                if (response.ok) {
                    betaForm.style.display = 'none';
                    document.getElementById('formSuccess').style.display = 'block';
                } else {
                    alert("Bir hata oluştu. Lütfen daha sonra tekrar deneyiniz.");
                }
            } catch (error) {
                alert("Bağlantı hatası. Lütfen internet bağlantınızı kontrol ediniz.");
            }
        });
    }
});